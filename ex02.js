function carre(nombre) {
	return nombre * nombre;
}

console.log(carre(4)); // Résultat attendu : 16
console.log(carre(2)); // Résultat attendu : 4
console.log(carre(5)); // Résultat attendu : 25
console.log(carre(9)); // Résultat attendu : 81
console.log(carre(-6)); // Résultat attendu : 36
console.log(carre(10)); // Résultat attendu : 100
