function sommeTroisNombres(a, b, c) {
	return a + b + c;
}

console.log(sommeTroisNombres(5, 10, 15)); // Résultat attendu : 30
console.log(sommeTroisNombres(0, -1, 2)); // Résultat attendu : 1
console.log(sommeTroisNombres(10, 98, 64)); // Résultat attendu : 172
console.log(sommeTroisNombres(34, -56, 4)); // Résultat attendu : -18
console.log(sommeTroisNombres(7, -3, 78)); // Résultat attendu : 82
